package br.com.bancorock;

public class agencia {
	
	int id_agencia;
	String conta ;
	String endereco;
	int gerente ;
	
	
	public agencia(int id_agencia, String conta, String endereco, int gerente) {
		super();
		this.id_agencia = id_agencia;
		this.conta = conta;
		this.endereco = endereco;
		this.gerente = gerente;
	}
	
	
}
